package funcoes

import kotlin.math.*

fun main() {

    println(max(5,10)) //retorna o máximo entre dois valores
    println(min(5,10)) //retorna o mínimo entre dois valores
    println(sqrt(45.67)) //função que mostra a raiz quadrada de um determinado número
    println(PI) //valor de PI
    println(E) //valor de Euler
    println(round(4.99)) //função que faz o arredondamento

}
