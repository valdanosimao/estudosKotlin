package controle_de_fluxo

/*
 * Comparação de valores
 * Maior >
 * Menor <
 * Menor igual <=
 * Maior igual >=
 */

/*
 * Comparação de igualdades
 * Igual ==
 * Diferente !=
 * Negação ! //inverte o valor da condição booleana, ex: se for true, com a negação !true ele se torna false
 */

/*
 * Tabela verdade
 *  a    |   b     | resultado
 * true  |  false  |  false
 * false |  true   |  false
 * false |  false  |  false
 * true  |  true   |  true
 *  ----------------------------
 * Operadores lógicos
 * Usados para avaliar uma relação entre valores
 * Um operador de conjunção só é avalidado como verdadeiro, caso os dois for verdade
 *
 * Conjunção && 'and' //uma condição 'e' outra conjunção
 * Disjunção || 'or' //só não é executada quando as duas condição for false
 *
 * Prioridades:
 * Parênteses
 * !Negação
 * &&Conjução
 * ||Disjunção
 */

/*
 * Operador Range
 * Usado apra avalier um intervalo de valores
 * if (n in 1..50){} // mesma coisa que: if(n >= 1 && n <= 50){}
 */